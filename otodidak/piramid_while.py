i = 1
while i <= 5:
    print( " " * (5-i) + "*" * i)
    i += 1

i = 1
while i <= 5:
    print("*" * i)
    i += 1

#i = 1 → nilai awal (baris pertama).
#while i <= 5: → loop jalan selama i belum lebih dari 5.
#print("*" * i) → mencetak bintang sesuai nilai i.
#i += 1 → tiap putaran, baris bertambah satu bintang.